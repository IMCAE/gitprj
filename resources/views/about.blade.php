	<h3>Production of chocolate</h3>

	<p> Chocolate is made from the kernels of fermented and roasted cocoa beans. The kernels are ground to form a pasty fluid chocolate liquor, which may be hardened in molds to form bitter (baking) chocolate, pressed to reduce the cocoa butter content and then pulverized to make cocoa powder, or mixed with sugar and additional cocoa butter to make sweet (eating) chocolate. The addition of dried or concentrated milk to sweet chocolate produces milk chocolate. </p>

